// XXVI - Faça um algoritmo que receba três números, calcule e mostre a multiplicação desses números

function multiplicacao(a, b, c) {
    return a * b * c;
};

console.log(multiplicacao(1, 2, 3) === 6);
console.log(multiplicacao(1.5, 2.34, 3) === 10.53);
console.log(multiplicacao(32, 7, 13) === 2912);